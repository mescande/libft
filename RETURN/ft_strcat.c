#include <string.h>

char	*ft_strcat(char *restrict s1, const char *restrict s2)
{
	size_t i;
	size_t j;

	i = 0;
	j = 0;
	while (s1[i] != '\0')
		i++;
	while (s2[++j - 1] != '\0')
		s1[i + j - 1] = s2[j - 1];
	return (s1);
}
